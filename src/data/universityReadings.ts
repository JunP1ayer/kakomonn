// 大学名検索用の関数
export function matchUniversityName(universityName: string, query: string): boolean {
  const readings = universityReadings[universityName]
  if (!readings) return false
  
  const lowerQuery = query.toLowerCase()
  return readings.some(reading => reading.includes(lowerQuery))
}